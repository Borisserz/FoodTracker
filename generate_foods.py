def _make_entry_ext(t):
    id_, name, calories, protein, fat, carbs, category = t
    return {
        "id": id_, "name": name, "calories": calories,
        "protein": round(protein, 1), "fat": round(fat, 1),
        "carbs": round(carbs, 1), "category": category
    }


def _write_language(combined_list, filename):
    import json, os
    entries = [_make_entry_ext(f) for f in combined_list]
    seen = set()
    unique = []
    for e in entries:
        if e["id"] not in seen:
            seen.add(e["id"])
            unique.append(e)
    out = os.path.join(os.path.dirname(os.path.abspath(__file__)), "FoodTracker", filename)
    root = os.path.join(os.path.dirname(os.path.abspath(__file__)), filename)
    for p in [out, root]:
        with open(p, "w", encoding="utf-8") as f:
            json.dump(unique, f, ensure_ascii=False, indent=2)
    print(f"  ✅ {filename}: {len(unique)} items, {os.path.getsize(out)/1024:.1f} KB")
    return unique


