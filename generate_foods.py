def make_entry(food_tuple):
    id_, name, calories, protein, fat, carbs, category = food_tuple
    return {
        "id": id_,
        "name": name,
        "calories": calories,
        "protein": round(protein, 1),
        "fat": round(fat, 1),
        "carbs": round(carbs, 1),
        "category": category
    }


def write_json(foods_list, filename):
    entries = [make_entry(f) for f in foods_list]
    # Deduplicate by ID (keep first occurrence)
    seen_ids = set()
    unique = []
    for e in entries:
        if e["id"] not in seen_ids:
            seen_ids.add(e["id"])
            unique.append(e)

    path = os.path.join(OUTPUT_DIR, filename)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(unique, f, ensure_ascii=False, indent=2)
    size_kb = os.path.getsize(path) / 1024
    print(f"✅ {filename}: {len(unique)} foods, {size_kb:.1f} KB")
    return unique


